package go.pemkott.appsandroidmobiletebingtinggi.model;

public class WaktuSift {

    String id, opd_id, tipe, inisial, masuk, pulang;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getOpd_id() {
        return opd_id;
    }

    public void setOpd_id(String opd_id) {
        this.opd_id = opd_id;
    }

    public String getTipe() {
        return tipe;
    }

    public void setTipe(String tipe) {
        this.tipe = tipe;
    }

    public String getInisial() {
        return inisial;
    }

    public void setInisial(String inisial) {
        this.inisial = inisial;
    }

    public String getMasuk() {
        return masuk;
    }

    public void setMasuk(String masuk) {
        this.masuk = masuk;
    }

    public String getPulang() {
        return pulang;
    }

    public void setPulang(String pulang) {
        this.pulang = pulang;
    }
}
